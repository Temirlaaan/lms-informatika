class Command(BaseCommand):
    help = 'Seed database with 5th grade Informatics curriculum data in Kazakh'

    def handle(self, *args, **options):
        self.stdout.write('Seeding database...')

        # Create test users
        self._create_users()

        # Create sections, topics, lessons, quizzes, questions, choices
        for section_data in SECTIONS:
            self._create_section(section_data)

        self.stdout.write(self.style.SUCCESS(
            'Successfully seeded: 3 users, 5 sections, 15 topics, '
            '15 lessons, 15 quizzes, 75 questions'
        ))

    def _create_users(self):
        # Superuser
        if not User.objects.filter(username='admin').exists():
            User.objects.create_superuser(
                username='admin',
                password='admin123',
                full_name='Администратор',
                role='teacher',
            )
            self.stdout.write('  Created superuser: admin / admin123')
        else:
            self.stdout.write('  Superuser admin already exists')

        # Teacher
        teacher, created = User.objects.get_or_create(
            username='teacher',
            defaults={
                'full_name': 'Айгүл Мұратқызы',
                'role': 'teacher',
            },
        )
        if created:
            teacher.set_password('teacher123')
            teacher.save()
            self.stdout.write('  Created teacher: teacher / teacher123')
        else:
            self.stdout.write('  Teacher already exists')

        # Student
        student, created = User.objects.get_or_create(
            username='student',
            defaults={
                'full_name': 'Арман Сериков',
                'role': 'student',
                'grade_class': '5А',
            },
        )
        if created:
            student.set_password('student123')
            student.save()
            self.stdout.write('  Created student: student / student123')
        else:
            self.stdout.write('  Student already exists')

    def _create_section(self, data):
        section, created = Section.objects.get_or_create(
            title=data['title'],
            defaults={
                'description': data['description'],
                'order': data['order'],
                'icon': data['icon'],
                'is_published': True,
            },
        )
        action = 'Created' if created else 'Found'
        self.stdout.write(f'  {action} section: {section.title}')

        for topic_data in data['topics']:
            self._create_topic(section, topic_data)

    def _create_topic(self, section, data):
        topic, created = Topic.objects.get_or_create(
            section=section,
            title=data['title'],
            defaults={
                'order': data['order'],
                'is_published': True,
            },
        )
        action = 'Created' if created else 'Found'
        self.stdout.write(f'    {action} topic: {topic.title}')

        # Create lesson
        lesson, _ = Lesson.objects.get_or_create(
            topic=topic,
            defaults={'content': data['lesson_content']},
        )

        # Create quiz
        quiz_data = data['quiz']
        quiz, quiz_created = Quiz.objects.get_or_create(
            topic=topic,
            defaults={
                'title': quiz_data['title'],
                'description': quiz_data['description'],
                'time_limit_minutes': 15,
                'passing_score': 60,
                'max_attempts': 3,
                'is_published': True,
            },
        )

        if quiz_created:
            for i, q_data in enumerate(quiz_data['questions']):
                question = Question.objects.create(
                    quiz=quiz,
                    text=q_data['text'],
                    question_type=q_data['type'],
                    points=q_data['points'],
                    order=i,
                )
                for j, (text, is_correct) in enumerate(q_data['choices']):
                    Choice.objects.create(
                        question=question,
                        text=text,
                        is_correct=is_correct,
                        order=j,
                    )
